function mbti(raw: string): MBTIType {
  const upper = raw.toUpperCase().trim();
  const valid: MBTIType[] = [
    'INTJ','INTP','ENTJ','ENTP','INFJ','INFP','ENFJ','ENFP',
    'ISTJ','ISFJ','ESTJ','ESFJ','ISTP','ISFP','ESTP','ESFP',
  ];
  if (valid.includes(upper as MBTIType)) return upper as MBTIType;
  // Default for "不知道" (don't know)
  return 'INFP';
}

function parseInterests(raw: string): string[] {
  // Split on Chinese/English commas, 、, newlines, and common delimiters
  return raw
    .split(/[,，、\n;；]+/)
    .map(s => s.trim())
    .filter(s => s.length > 0 && s.length < 50);
}

